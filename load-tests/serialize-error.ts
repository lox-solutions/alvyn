import type { SerializedHttpError } from "./http-types";

export function serializeError(error: unknown): SerializedHttpError {
  if (error instanceof Error) {
    return {
      name: error.name,
      message: error.message,
      ...(error.stack === undefined ? {} : { stack: error.stack }),
    };
  }
  return { name: "UnknownError", message: String(error) };
}
